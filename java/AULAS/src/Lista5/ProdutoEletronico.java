package Lista5;

public class ProdutoEletronico {
	public double preco;
	public String categoria;
	public String voltagem;
	public boolean smart;
	public ProdutoEletronico(double preco, String categoria, String voltagem, boolean smart) {
		super();
		this.preco = preco;
		this.categoria = categoria;
		this.voltagem = voltagem;
		this.smart = smart;
	}
	
	
}
